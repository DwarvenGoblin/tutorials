	public static int getWidth(){
		return map.GetLength(1);
	}

	public static int getHeight() {
		return map.GetLength(0);
	}

	static public float getCost(float toX,float toY, float fromX, float fromY) {
		if (!isPassable(toX, toY)){
			return -1f;
		}
		if (toX!=fromX && toY != fromY) {
			if (!isPassable(fromX,toY) || !isPassable(toX,fromY)){
				return -1;
			}
			return SpaceConstants.GRID_DIAG;
		}
		return 1;
	}

	public static bool isPassable(float x, float y){
		Vector2 snapped = GridMath.snapToGridVertex(x,y);
		return isPassable((int)snapped.x,(int)snapped.y);
	}

	public static bool isPassable(int x, int y){
		return map[y,x] == 1;
	}
	
